                        Mark M. Davis,
                            <span>PhD</span>
                        </h5>
                        <p>
                        Senior Scientific Advisor
                        </p>
                        <div class="team-description">
                            <img src="./assets/img/mark-davis.png" alt="Team Image" class="team-head">
                            <p class="py-2">Mark M. Davis, PhD. is a world-renowned immunologist and distinguished scientist. Dr. Davis was the founding Director of the Stanford Institute for Immunity, Transplantation and Infection (ITI), which he turned it into an international leader in the development of transformative technologies and approaches to better understand the human immune system and its responses to infectious diseases and autoimmunity. He is currently a Professor of Microbiology and Immunology and the Director of the Center for Human Systems Immunology and co-Director of the Parker Center for the Immunotherapy of Cancer at Stanford. 

                            </p>
                            <p class="py-2">Dr. Davis is well known for identifying the first T-cell receptor genes, which are responsible for T lymphocytes ability to “see” foreign pathogens, such as viruses, solving a major mystery in immunology at that time. He and his research group have made many subsequent discoveries about this type of molecule, demonstrating that T-cells are able to detect and respond to even a single molecule of their ligand-fragments of antigens bound to Major Histocompatibility Complex cell surface molecules. He also developed a novel way of labeling specific T lymphocytes according to the molecules that they recognize, and this procedure is now an important method in many clinical and basic studies of T-cell activity, as are other technologies developed by his group.</p>
                            <p class="py-2">According to research.com, Dr. Davis has authored over 500 publications with over 125,000 citations. He has received numerous awards and honors for his work, including memberships in the National Academy of Sciences, The National Academy of Medicine and the Royal Society. </p>
